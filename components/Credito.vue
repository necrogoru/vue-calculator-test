<template>
  <div class="row">
    <h1>Credito</h1>
    <div class="row">
      <div class="col l4">
        <h5>Cuota</h5>
        <h1>$ {{toCurrency(cuota)}}</h1>
      </div>
      <div class="col l4">
        <h5>Ingresos personales o familiares</h5>
        <h1>$ {{toCurrency(ingresos)}}</h1>
      </div>
      <div class="col l4">
        <h5>Desceuento FRECH NO VIS</h5>
        <h1>$ {{toCurrency(descuentoFrech)}}</h1>
      </div>
    </div>
  </div>
</template>

<script>
  export default {
    props: ['calcParams', 'money'],
    data(){
      return {
      }
    },
    computed: {
      cuota(){
        let i = this.calcParams.interest / 100
        let n = this.calcParams.years
        let vp = this.money
        var cuota = vp * ((Math.pow(1 + i, n) * i) / (Math.pow(1 + i, n) - 1)) / 12
        return Math.round(cuota / 1000, 0) * 1000
      },
      ingresos(){
        return Math.round(this.cuota * this.calcParams.valueIngresos / 1000, 0) * 1000
      },
      descuentoFrech(){
        return Math.round(this.cuota * this.calcParams.frechNoVis / 1000, 0) * 1000
      }

    },
    methods: {
      // currency format
      toCurrency(value) {
        var val = (value/1).toFixed(0).replace('.', ',')
        return val.toString().replace(/\B(?=(\d{3})+(?!\d))/g, ".")
      },
    }
  }
</script>

